İsimlendirme şekillerinin olmasının nedeni programlama içerisinde boşluk bırakarak yazım yapılamamaktadır.
Kelimeleri birbirlerinden ayırmak için isimlendirme şekilleri kullanılmaktadır.
4 farklı isimlendirme şekli vardır. Bunlar camel case, pascal case, snake case ve kebab case olarak 4'e ayrılır.

1-) Camel Case: Kelimeleri birbirinden ayırmak için büyük harf kullanılır fakat ilk harf her zaman küçük olarak kullanılır.
Büyük harf küçük harf duyarlılığı olan programlama dillerinde kullanılır. Genellikle değişken isimlendirmelerinde ve fonksiyon isimlerinde kullanılır.
Örnek olarak "camelCase" verilebilir.

2-) Pascal Case: Camel case gibi kelimeleri birbirlerinden ayırmak için büyük harf kullanılır fakat camel case'den farklı olarak tüm kelimeler büyük harfle başlar.
Büyük harf küçük harf duyarlılığı olan programlama dillerinde kullanılır. Genellikle sınıf tanımlamaları yapılırken kullanılır.
Örnek olarak "PascalCase" verilebilir.

3-) Snake Case: Kelimeleri birbirinden ayrımak için alt çizgi(_) kullanılır.
Veri tabanlarında büyük küçük harf duyarlılığı olmadığı için veri tabanlarında tablo isimleri veya kolon isimlerinde kullanılır.
Ayrıca bazı programlama dillerindeki bazı sabitleri tanımlamak için de kullanılır.(INTEGER_MAX_VALUE)
Örnek olarak "snake_case" veya "SNAKE_CASE" verilebilir.

4-) Kebab Case: Kelimeleri birbirinden ayırmak için tire(-) kullanılır.
Genellikle internet sitelerindeki url tanımlamalarında kullanılır. Ayrıca html ve css içerisinde kullanılır.
Örnek olarak "kebab-case" verilebilir.


*/